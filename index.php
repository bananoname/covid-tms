          <h2>Covid-19 Symptoms</h2>
          <hr>
          <p><strong>High Fever (2-14 days)</strong><br>A high-grade fever (typically above 38°C/100.4°F) is one of the earliest and most common symptoms. It can last from a few days up to two weeks. Fever reflects the body’s natural immune response to viral infection. In severe cases, the fever can be persistent and difficult to control, sometimes requiring hospitalization</p>
          <hr>
          <p><strong>Dry Cough (2-14 days)</strong><br>A persistent, dry (non-productive) cough often develops early. This type of cough does not produce mucus and can become very irritating. It can worsen over time, leading to chest discomfort. In severe cases, it may indicate lower respiratory tract involvement like pneumonia.</p>
          <hr>
          <p><strong>Shortness of breath</strong><br>Difficulty breathing or a feeling of not getting enough air can emerge, especially in moderate to severe cases. It is a warning sign of possible lung involvement, such as viral pneumonia or acute respiratory distress syndrome (ARDS), which may require urgent medical care.</p>
          <hr>
          <p><strong>Loss of Taste or Smell</strong><br>TSudden and complete loss of taste (ageusia) or smell (anosmia) is a highly specific symptom, often occurring even in the absence of nasal congestion. It can be an early indicator of COVID-19 infection and may persist for weeks after other symptoms have resolved.</p>
          <hr>
          <p><strong>Fatigue</strong><br>Profound exhaustion and weakness can occur even without a high fever. This fatigue may be overwhelming, making daily activities difficult. In some cases, fatigue can persist for weeks (known as post-viral fatigue syndrome or "long COVID").</p>
          <hr>
          <p><strong>Muscle or Body Aches</strong><br>Myalgia, or widespread muscle pain, is commonly reported. It may feel like the muscle soreness experienced after intense physical exertion, and can range from mild discomfort to debilitating pain that affects mobility.</p>
          <hr>
          <p><strong>Headache</strong><br>Headaches associated with COVID-19 can be frequent, persistent, and sometimes severe. They may feel like tension-type headaches or even migraines, and often occur early in the illness alongside fever and fatigue.</p>
          <hr>
          <p><strong>Sore Throat</strong><br>A dry or mildly painful throat, without significant nasal symptoms, can be an early sign of infection. It can worsen when talking or swallowing and may precede the onset of cough and fever.</p>
          <hr>
          <p><strong>Congestion or Runny Nose</strong><br>Though less common compared to other respiratory infections like the flu or cold, some COVID-19 patients report nasal congestion or a runny nose. These symptoms are typically mild but should still prompt caution and testing.</p>
          <hr>
          <p><strong>Nausea or Vomiting</strong><br>Gastrointestinal symptoms are increasingly recognized in COVID-19 cases. Some individuals experience nausea, vomiting, or stomach discomfort, sometimes even before respiratory symptoms appear.</p>
          <hr>
          <p><strong>Diarrhea</strong><br>Loose or watery stools have been observed in a subset of patients. Digestive symptoms can be early signs of COVID-19, and in some cases, diarrhea can lead to dehydration if not properly managed.</p>
